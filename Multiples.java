
//program to print the first ten multiples of 4 using a `while` loop:

public class Multiples {
    public static void main(String[] args) {
        int count = 0;
        int num = 4;
        
        while (count < 10) {
            System.out.println(num);
            num += 4;
            count++;
        }
    }

    
}
